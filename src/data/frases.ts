/**
 * Devuelve la frase correspondiente a la semana actual.
 * Cambia cada lunes y no repite por ~19 años (1000 semanas).
 */
export function getFraseSemanal(): string {
  // Época fija: lunes 1 de enero de 2024
  const epoch = new Date('2024-01-01T00:00:00Z');
  const now = new Date();
  const diffMs = now.getTime() - epoch.getTime();
  const diffWeeks = Math.floor(diffMs / (7 * 24 * 60 * 60 * 1000));
  return frases[((diffWeeks % frases.length) + frases.length) % frases.length];
}
